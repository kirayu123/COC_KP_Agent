const moduleScenes: Scene[] = storyNodes.map((node) => ({
  id: node.id,
  title: node.title,
  location: node.location,
  summary: node.summary,
  pressure: node.risks[0] ?? '等待 KP 根据玩家行动推进风险。',
  sourceRef: `modules/liaoque_huanmeng/scenes/${node.id}.md`,
}))

const moduleClues: Clue[] = storyNodes.flatMap((node) =>
  node.clues.map((clue, index) => ({
    id: `${node.id}-clue-${index + 1}`,
    title: clue.title,
    content: clue.playerText,
    status: 'hidden',
    sceneId: node.id,
    relatedNpcIds: [],
    revealHint: clue.keeperText,
  })),
)

const campaignClocks: ProgressClock[] = moduleClocks.map((clock, index) => ({
  id: `clock-${index + 1}`,
  title: clock.title,
  current: clock.current,
  max: clock.max,
  consequence: clock.consequence,
}))

